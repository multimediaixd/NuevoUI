package com.example.nuevoui;

public class item_pictograma {

    int pictigrama;
    String nombre_pictograma;


    public item_pictograma(int pictigrama, String nombre_pictograma) {
        this.pictigrama = pictigrama;
        this.nombre_pictograma = nombre_pictograma;
    }

    public int getPictigrama() {
        return pictigrama;
    }

    public void setPictigrama(int pictigrama) {
        this.pictigrama = pictigrama;
    }

    public String getNombre_pictograma() {
        return nombre_pictograma;
    }

    public void setNombre_pictograma(String nombre_pictograma) {
        this.nombre_pictograma = nombre_pictograma;
    }
}
